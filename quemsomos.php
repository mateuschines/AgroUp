    <p>Por serem admiradores e apaixonados pelo trabalho árduo do homem do campo na Agropecuária, que oferece alimentos de excelente qualidade a mesa do consumidor, dois alunos do curso de Tecnologia em Marketing da Faculdade Alfa de Umuarama/PR, Alessandro Limeira e Felipe Viana, vislumbraram oportunidades de oferecer à região onde residem, serviços mercadológicos que o segmento necessita.
        As empresas sujeitas a maior risco são aquelas que não conseguem monitorar seus clientes e concorrentes com cuidado e aperfeiçoar sempre suas ofertas de valor.  Fonte: P. Kotler, K. Keller. Em: Administração de Marketing p. 3.
        Por este e outros motivos, um plano de marketing é uma de várias ferramentas essenciais para o levantamento de informações relevantes e detalhadas sobre o segmento de atuação. Ele permite a criação de competitividade entre as empresas já existentes através da inclusão de valores à marca e aos serviços prestados pela empresa.</p>
